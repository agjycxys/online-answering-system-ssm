package com.agjycxys.domain;

public class MSG {
	private int msgid;
	private String msgaccount;
	private String msgname;
	private String msgphone;
	private String msgpwd;
	private int kcid;

	public int getMsgid() {
		return msgid;
	}

	public void setMsgid(int msgid) {
		this.msgid = msgid;
	}

	public String getMsgname() {
		return msgname;
	}

	public void setMsgname(String msgname) {
		this.msgname = msgname;
	}

	public String getMsgphone() {
		return msgphone;
	}

	public void setMsgphone(String msgphone) {
		this.msgphone = msgphone;
	}

	public String getMsgpwd() {
		return msgpwd;
	}

	public void setMsgpwd(String msgpwd) {
		this.msgpwd = msgpwd;
	}

	public int getKcid() {
		return kcid;
	}

	public void setKcid(int kcid) {
		this.kcid = kcid;
	}

	public String getMsgaccount() {
		return msgaccount;
	}

	public void setMsgaccount(String msgaccount) {
		this.msgaccount = msgaccount;
	}
	

}
